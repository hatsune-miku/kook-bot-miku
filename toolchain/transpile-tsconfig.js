const yaml = require('js-yaml')
const fs = require('fs')

const config = yaml.load(fs.readFileSync('tsconfig.yaml', 'utf8'))

let serialized = JSON.stringify(config, null, 2)
serialized = '// GENERATED BY TOOLCHAIN/TRANSPILE-TSCONFIG.JS\n\n' + serialized

fs.writeFileSync('tsconfig.json', serialized)
